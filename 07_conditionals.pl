use strict;
use warnings;

# Basic IF

if (1) {

	print "Evaluates as true\n";

}

# IF ELSE

if (0) {

	print "Evaluate as true\n";

} else {

	print "Evaluates as false\n";
}

# IF ELSIF ELSE
if (0) {

        print "1. Evaluate as true\n";

} elsif (0) {

	print "2. Evaluates as true\n";

} else {

        print "3. Evaluates as false\n";
}

# Unless

unless (1) {

	print "UNLESS Evaluates as false\n";

}

